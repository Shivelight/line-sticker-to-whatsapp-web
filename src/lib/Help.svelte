<p>
  First you need to get LINE sticker pack ID or store URL that you want to convert either from <a href="https://store.line.me/" target="_blank" rel="noreferrer">store.line.me</a> or from the app.
</p>
<div class="left">
  <p>Example:</p>
  <pre>1. https://store.line.me/stickershop/product/<span>1344676</span> (Store URL)</pre>
  <pre>2. https://line.me/S/sticker/<span>1344676</span> (App URL)</pre>
  <pre>3. <span>1344676</span> (ID Only)</pre>
  <pre><span>*</span> If the URL includes more paths or parameters or seems to be longer than the examples, then don't worry, it is also accepted as long as the ID is present.</pre>
</div>
<p>
  Enter the sticker pack ID or URL into the input field and then press <kbd>Get Data</kbd> button. If the sticker pack is found, its detail will be displayed.
</p>
<p>
  If everything is correct, press <kbd>Convert to WhatsApp Sticker</kbd> button to start the process. Download will start automatically after the process is finished; make sure to confirm if needed.
</p>
<h3>Android: What Next?</h3>
<p>To install the sticker pack into WhatsApp, you need to extract the content of the downloaded <code>.zip</code> file first. Then you can use any of your favorite personal sticker maker to load the sticker pack, but if you don't have any, I recommend <a href="https://play.google.com/store/apps/details?id=com.dstukalov.walocalstoragestickers" target="_blank" rel=" noreferrer">Personal stickers for WhatsApp</a> because of its simplicity.</p>
